public class Shark {
    private String name, breed;
    private int age, length, weight, numOfTeeth;

    public Shark(String name, String breed, int age, int length, int weight, int numOfTeeth){
        this.name = name;
        this.breed = breed;
        this.age = age;
        this.length = length;
        this.weight = weight;
        this.numOfTeeth = numOfTeeth;
    }
}

